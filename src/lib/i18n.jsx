export const DICT = { zh, en }

// '' / unknown → follow the system language
export function resolveLang(pref) {
  if (pref === 'zh' || pref === 'en') return pref
  const nav = (typeof navigator !== 'undefined' && navigator.language) || 'zh'
  return nav.toLowerCase().startsWith('zh') ? 'zh' : 'en'
}

export function makeT(lang) {
  const d = DICT[lang] || DICT.zh
  return (key, vars) => {
    const e = d[key] ?? DICT.zh[key] ?? key
    if (typeof e === 'function') return e(vars || {})
    if (typeof e === 'string' && vars) return e.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ''))
    return e
  }
}

// React hook — language comes from profile.lang ('' = auto)
export function useI18n() {
  const { state } = useStore()
  const lang = resolveLang(state.profile.lang)
  const t = useMemo(() => makeT(lang), [lang])
  return { t, lang }
}
